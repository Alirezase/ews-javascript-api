
/**
 * Defines the status of group members.
 */
export enum MemberStatus {
    
    /**
     * The member is unrecognized.
     */
    Unrecognized = 0,
    
    /**
     * The member is normal.
     */
    Normal = 1,
    
    /**
     * The member is demoted.
     */
    Demoted = 2
}