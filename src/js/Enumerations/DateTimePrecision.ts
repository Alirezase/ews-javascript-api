
/**
 * Defines the precision for returned DateTime values
 */
export enum DateTimePrecision {
    
    /**
     * Default value.  No SOAP header emitted.
     */
    Default = 0,
    
    /**
     * Seconds
     */
    Seconds = 1,
    
    /**
     * Milliseconds
     */
    Milliseconds = 2
}