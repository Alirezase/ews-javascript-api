
/**
 * Defines the sensitivity of an item.
 */
export enum Sensitivity {
    
    /**
     * The item has a normal sensitivity.
     */
    Normal = 0,
    
    /**
     * The item is personal.
     */
    Personal = 1,
    
    /**
     * The item is private.
     */
    Private = 2,
    
    /**
     * The item is confidential.
     */
    Confidential = 3
}